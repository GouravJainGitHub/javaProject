package SET10;

public class Set10_Q3 {
    public static void main(String args[]) {
        // Write a program to find the sum up to a number.
        int n = 6;
        int i = 0;
        int sum = 0;
        while (i < n) {
            sum = sum + i;
            System.out.println(sum);
            i++;
        }
    }
}
